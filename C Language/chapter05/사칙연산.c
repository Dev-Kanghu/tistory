#include <stdio.h>

int main(void) {
    int num = 2;
    printf("num은 %d입니다.\n", num);

    num = num + 3;
    printf("num은 %d입니다.\n", num);

    num = num - 1;
    printf("num은 %d입니다.\n", num);

    num = num * 3;
    printf("num은 %d입니다.\n", num);

    num = num / 2;
    printf("num은 %d입니다.\n", num);
    return 0;
}
